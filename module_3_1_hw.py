calls = 0


def count_calls():
    global calls
    calls += 1


def string_info(string):
    count_calls()
    return len(string), string.upper(), string.lower()


def is_contains(string, list_to_search):
    count_calls()

    for str_ in list_to_search:
        a = str_.lower()
        b = string.lower()
        c = a.find(b)
        if str_.lower().find(string.lower()) >= 0:
            return True

    return False


